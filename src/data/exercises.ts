export function getExercisesBySlot(slot: MuscleGroupSlot): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter((ex) => ex.slot === slot)
}

export function getExercisesByEquipment(equipment: EquipmentType[]): ExerciseDefinition[] {
  if (equipment.length === 0) return EXERCISE_LIBRARY.filter(e => e.equipment.includes('bodyweight'))
  return EXERCISE_LIBRARY.filter((ex) =>
    ex.equipment.some((eq) => equipment.includes(eq)) || ex.equipment.includes('bodyweight')
  )
}

export function getExerciseById(id: string): ExerciseDefinition | undefined {
  return EXERCISE_LIBRARY.find((ex) => ex.id === id)
}
